// Pokhara center coordinates for distance calculation fallback
export const POKHARA_CENTER = {
  lat: 28.2096,
  lng: 83.9856,
};

// Haversine formula to calculate distance between two coordinates in km
export const calculateDistance = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const R = 6371; // Earth's radius in kilometers
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * 
    Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return Math.round(R * c * 10) / 10; // Round to 1 decimal place
};

// Get user's current position with fallback
export const getUserPosition = (): Promise<{ lat: number; lng: number }> => {
  return new Promise((resolve) => {
    if (navigator.geolocation) {
      navigator.geolocation.getCurrentPosition(
        (position) => {
          resolve({
            lat: position.coords.latitude,
            lng: position.coords.longitude,
          });
        },
        () => {
          // Fallback to Pokhara center if permission denied or error
          resolve(POKHARA_CENTER);
        },
        { timeout: 5000 }
      );
    } else {
      // Fallback for browsers without geolocation
      resolve(POKHARA_CENTER);
    }
  });
};

// Sort cafes by distance from user location
export const sortCafesByDistance = (
  cafesList: Cafe[], 
  userLat: number, 
  userLng: number
): Cafe[] => {
  return [...cafesList].sort((a, b) => {
    const distA = calculateDistance(userLat, userLng, a.location.lat, a.location.lng);
    const distB = calculateDistance(userLat, userLng, b.location.lat, b.location.lng);
    return distA - distB;
  });
};

// Filter products by flavor profiles
export const filterProductsByFlavors = (
  products: CoffeeProduct[], 
  selectedFlavorIds: string[]
): CoffeeProduct[] => {
  if (selectedFlavorIds.length === 0) return products;
  
  return products.filter(product => {
    if (!product.flavorProfileIds) return false;
    // Product matches if it has ANY of the selected flavors
    return selectedFlavorIds.some(flavorId => 
      product.flavorProfileIds?.includes(flavorId)
    );
  });
};

// Get products that match ALL selected flavors (for "perfect match")
export const getPerfectMatches = (
  products: CoffeeProduct[], 
  selectedFlavorIds: string[]
): CoffeeProduct[] => {
  if (selectedFlavorIds.length === 0) return [];
  
  return products.filter(product => {
    if (!product.flavorProfileIds) return false;
    // Product matches if it has ALL of the selected flavors
    return selectedFlavorIds.every(flavorId => 
      product.flavorProfileIds?.includes(flavorId)
    );
  });
};
